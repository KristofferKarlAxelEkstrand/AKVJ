import fs from 'fs';
import path from 'path';

const teamDir = path.join(import.meta.dirname, '..');
const tasksDir = path.join(teamDir, 'tasks');
const doneDir = path.join(tasksDir, 'done');
const slackDir = path.join(teamDir, 'slack');
const dashboardPath = path.join(slackDir, 'general', 'team-dashboard.md');

// Helper to parse simple YAML frontmatter
function parseFrontmatter(content) {
  const match = content.match(/^---\n([\s\S]*?)\n---/);
  if (!match) {
    return null;
  }
  const yaml = match[1];
  const result = {};
  yaml.split('\n').forEach(line => {
    const colonIdx = line.indexOf(':');
    if (colonIdx > 0) {
      const key = line.substring(0, colonIdx).trim();
      const value = line.substring(colonIdx + 1).trim();
      result[key] = value;
    }
  });
  return result;
}

function getTasks(dir) {
  if (!fs.existsSync(dir)) {
    return [];
  }
  return fs.readdirSync(dir)
    .filter(f => f.endsWith('.md'))
    .map(file => {
      const filePath = path.join(dir, file);
      const content = fs.readFileSync(filePath, 'utf8');
      const meta = parseFrontmatter(content) || {};
      // Try to get a title from the first # Heading
      const titleMatch = content.match(/^#\s+(.*)/m);
      const title = titleMatch ? titleMatch[1].trim() : file;
      const mtime = fs.statSync(filePath).mtime.getTime();
      return { file, title, meta, mtime };
    });
}

const activeTasks = getTasks(tasksDir);
const doneTasks = getTasks(doneDir);

function getAssignedTasks() {
  const assigned = { 'akvj-developer': [], 'mainframe-developer': [], 'qa-reviewer': [] };
  const prefixFilters = {
    'akvj-developer': /^\[TASK\]/,
    'mainframe-developer': /^\[TASK\]/,
    'qa-reviewer': /^\[TASK\]-review-/,
  };
  for (const dev of Object.keys(assigned)) {
    const dir = path.join(slackDir, dev);
    if (fs.existsSync(dir)) {
      const files = fs.readdirSync(dir).filter(f => f.endsWith('.md') && prefixFilters[dev].test(f));
      assigned[dev] = files;
    }
  }
  return assigned;
}

const assigned = getAssignedTasks();

// Sort tasks based on meta
const backlog = activeTasks.filter(t => t.meta.status === 'backlog' || !t.meta.status);
const inProgress = activeTasks.filter(t => t.meta.status === 'in-progress');

// Generate Markdown
let md = `# Team Dashboard\n\n*Auto-generated by \`generate-dashboard.js\`.*\n\n`;

md += `## 📋 Backlog (${backlog.length})\n`;
backlog.forEach(t => {
  md += `- **${t.file}**: ${t.title} (Priority: ${t.meta.priority || 'none'})\n`;
});

md += `\n## 🚀 In Progress\n`;
['akvj-developer', 'mainframe-developer'].forEach(dev => {
  md += `### ${dev}\n`;
  const devTasks = inProgress.filter(t => t.meta.assignee === dev);
  if (devTasks.length > 0) {
    devTasks.forEach(t => md += `- **${t.file}**: ${t.title}\n`);
  } else {
    md += `- *No tasks assigned in frontmatter*\n`;
  }
  if (assigned[dev].length > 0) {
    md += `\n**Inbox / Active Files:**\n`;
    assigned[dev].forEach(f => md += `- ${f}\n`);
  }
  md += `\n`;
});

md += `## 🔍 Pending QA Audit\n`;
if (assigned['qa-reviewer'].length > 0) {
  assigned['qa-reviewer'].forEach(f => md += `- ${f}\n`);
} else {
  md += `- *No tasks pending review*\n`;
}

md += `\n## ✅ Done (Recent 15)\n`;
const recentDone = doneTasks.sort((a, b) => a.mtime - b.mtime).slice(-15);
if (recentDone.length > 0) {
  recentDone.forEach(t => {
    md += `- **${t.file}**: ${t.title}\n`;
  });
} else {
  md += `- *No completed tasks yet*\n`;
}

fs.writeFileSync(dashboardPath, md);
console.log(`Dashboard successfully generated at ${dashboardPath}`);
